using UnityEngine;
using System.Collections;

public class PlayerBoatController : MonoBehaviour {
	public float speed = 1f;
	public float fastSpeed = 1.5f;
	public float slowSpeed = 0.5f;
	public float turnSpeed = 0.2f;
	private int moveRate = 1;
	public float encounterRange = 5;
	
	public bool paused = false;
	
	// Use this for initialization
	void Start () {
	}
	
	// Update is called once per frame
	void FixedUpdate () {
	if(!paused)
	{
	
		if(moveRate == 0)
		{
			gameObject.GetComponent<Rigidbody>().velocity = Input.GetAxis("Vertical")*slowSpeed*transform.forward;
			transform.Rotate(Input.GetAxis("Horizontal")*turnSpeed*transform.up);
		}
	
		if(moveRate == 1)
		{
			gameObject.GetComponent<Rigidbody>().velocity = Input.GetAxis("Vertical")*speed*transform.forward;
			transform.Rotate(Input.GetAxis("Horizontal")*turnSpeed*transform.up);
		}
	
		if(moveRate == 2)
		{
			gameObject.GetComponent<Rigidbody>().velocity = Input.GetAxis("Vertical")*fastSpeed*transform.forward;
			transform.Rotate(Input.GetAxis("Horizontal")*turnSpeed*transform.up);
		}
	}
	}
	
	public void SlowSpeed()
	{
		moveRate = 0;
	}
	
	public void MediumSpeed()
	{
		moveRate = 1;
	}
	
	public void HighSpeed()
	{
		moveRate = 2;
	}
	
	public void Pause()
	{
		paused = true;
	}
	
	public void Unpause()
	{
		paused = false;
	}
	
	//used for anchoring
	public void togglePause()
	{
		if(paused)
		{
			paused = false;
		}
		
		else if(!paused)
		{
			paused = true;
			Collider[] objectsNear = Physics.OverlapSphere(transform.position, encounterRange);
			for(int i = 0; i< objectsNear.Length;i++)
			{
				if (objectsNear[i].gameObject.tag.Equals("Island"))
				{
				objectsNear[i].GetComponent<IslandEventScript>().HaveEvent();
				}
			
			}
		}
	}
}
